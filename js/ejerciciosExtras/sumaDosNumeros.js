// Realizar una suma de dos números:  Crear un algoritmo que permita al usuario ingresar dos números,
// realizar la suma de ambos y mostrar el resultado.

const numero1 = parseInt(prompt("Ingresa el primer numero"))
const numero2 = parseInt(prompt("Ingresa el segundo numero"))
const resultado = numero1 + numero2

document.writeln("La suma de los dos numeros es: " + resultado)